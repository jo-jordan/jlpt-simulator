export function t(
  language: LanguageCode,
  key: string,
  params?: Record<string, string | number>,
) {
  const value = messages[language][key] ?? messages.en[key] ?? key

  if (typeof value === 'function') {
    return value(params ?? {})
  }

  return value
}
